export const getTailWindBg = (color) => {
  switch (color) {
    case "#000000":
      return "bg-[#000000]";
    case "#F37C34":
      return "bg-[#F37C34]";
    case "#777777":
      return "bg-[#777777]";
    case "#DDDDDD":
      return "bg-[#DDDDDD]";
    case "#FFFFFF":
      return "bg-[#FFFFFF]";
    default:
      return "bg-[#7D4AEA]";
  }
};
